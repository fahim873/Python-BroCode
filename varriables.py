#varriable = A container for a value (String, Integer, Float, Boolen)
#            A varriable behaves as if it was the value it contains

#strings
first_name = "Mohammad Fahim"
food = "Pizza"
email = "fahim873@gmail.com"

print(f"Hello {first_name}")
print(f"You like {food}")
print(f"Your E-mail is: {email}")
print("\n")

#Integers
age = 22
quantity = 3
num_of_std = 25
print(f"Your are {age} years old")
print(f"You can carry maximum {quantity} devices")
print(f"You lab section have {num_of_std} student")
print("\n")

#Float
price = 10.99
gpa = 3.89
print(f"The price of this package is ${price} (Fixed)")
print(f"My CGPA in this semester is {gpa}")

#Boolean
is_student = False
print(f"Are you a student?: {is_student}")
print("\n")
if is_student:
  print("You are a student")
else:
  print("You are not a student")
print("\n")

online = True
if online:
  print("You are online")
else:
  print("You are offline")
print("\n")

